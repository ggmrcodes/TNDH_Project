/**
 * seed-e2e-accounts.mjs — provision throwaway accounts for the chat E2E.
 *
 * Two modes (the verify step in between is admin-gated, so it's yours):
 *
 *   1. CREATE:  node --env-file=.env scripts/seed-e2e-accounts.mjs create
 *        Signs up a patient + a clinician (real auth users), inserts the
 *        clinician_profiles row (verified=false), and WRITES the 4 creds into
 *        .env.e2e. Prints the one SQL line you must run to verify the clinician.
 *
 *   2. (you) verify the clinician in the SQL editor — the printed line.
 *
 *   3. LINK:    node --env-file=.env --env-file=.env.e2e scripts/seed-e2e-accounts.mjs link
 *        Clinician creates the link (pending); patient approves it (active).
 *        After this, run scripts/chat-realtime-e2e.mjs.
 *
 * Uses only the public URL + anon key. Cannot delete the auth users afterward
 * (no service-role) — clean up the e2e-* users in the dashboard when done.
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync } from 'node:fs';
import { randomBytes } from 'node:crypto';

const URL = process.env.EXPO_PUBLIC_SUPABASE_URL;
const ANON = process.env.EXPO_PUBLIC_SUPABASE_ANON_KEY;
const die = (m) => { console.error(`\n✗ ${m}\n`); process.exit(1); };
if (!URL || !ANON) die('EXPO_PUBLIC_SUPABASE_URL / _ANON_KEY missing. Run with: node --env-file=.env ...');

const mode = process.argv[2] ?? 'create';
const makeClient = () =>
  createClient(URL, ANON, { auth: { persistSession: false, autoRefreshToken: false } });

// signUp, then ensure we actually hold a session (autoconfirm). If the project
// requires email confirmation, signUp returns no session and sign-in fails —
// report that clearly because we can't proceed without a confirmed user.
async function signUpAndSession(client, email, password, label) {
  const { data, error } = await client.auth.signUp({ email, password });
  if (error) die(`signUp failed for ${label} (${email}): ${error.message}`);
  if (data.session && data.user) return data.user.id;
  const { data: si, error: siErr } = await client.auth.signInWithPassword({ email, password });
  if (siErr || !si?.user) {
    die(`Created ${label} but no session — the project likely requires email confirmation `
      + `(Auth → Providers → Email → "Confirm email" ON). Turn it off for the test project, or `
      + `confirm ${email} manually, then re-run.`);
  }
  return si.user.id;
}

async function create() {
  const ts = Date.now();
  const pw = `Aa1!${randomBytes(12).toString('base64url')}`; // meets complexity rules
  const patientEmail = `e2e-patient-${ts}@example.com`;
  const clinEmail = `e2e-clin-${ts}@example.com`;

  console.log(`\nProvisioning E2E accounts on ${URL}\n`);

  const pc = makeClient();
  const patientId = await signUpAndSession(pc, patientEmail, pw, 'patient');
  console.log(`  ✓ patient   ${patientEmail}  (${patientId.slice(0, 8)}…)`);

  const cc = makeClient();
  const clinicianId = await signUpAndSession(cc, clinEmail, pw, 'clinician');
  const { error: cpErr } = await cc.from('clinician_profiles').insert({
    user_id: clinicianId,
    full_name: 'E2E Test Clinician',
    license_number: `E2E-${ts}`,
    hospital_affiliation: 'E2E Test Hospital',
    verified: false,
  });
  if (cpErr) die(`clinician_profiles insert failed: ${cpErr.message}`);
  console.log(`  ✓ clinician ${clinEmail}  (${clinicianId.slice(0, 8)}…)  [unverified]`);

  writeFileSync('.env.e2e',
    `# Generated by seed-e2e-accounts.mjs at ${new Date().toISOString()} — throwaway test accounts\n`
    + `E2E_PATIENT_EMAIL=${patientEmail}\n`
    + `E2E_PATIENT_PASSWORD=${pw}\n`
    + `E2E_CLINICIAN_EMAIL=${clinEmail}\n`
    + `E2E_CLINICIAN_PASSWORD=${pw}\n`);
  console.log(`  ✓ wrote .env.e2e\n`);

  console.log(`NEXT — run this ONE line in the Supabase SQL editor to verify the clinician:\n`);
  console.log(`  update public.clinician_profiles set verified = true, verified_at = now()`);
  console.log(`    where user_id = '${clinicianId}';\n`);
  console.log(`Then tell me "verified" and I'll create + approve the link and run the E2E.\n`);
}

async function link() {
  const {
    E2E_PATIENT_EMAIL, E2E_PATIENT_PASSWORD,
    E2E_CLINICIAN_EMAIL, E2E_CLINICIAN_PASSWORD,
  } = process.env;
  if (!E2E_PATIENT_EMAIL || !E2E_CLINICIAN_EMAIL) die('Run create mode first (no .env.e2e creds found).');

  const pc = makeClient(), cc = makeClient();
  const { data: pd, error: pe } = await pc.auth.signInWithPassword({ email: E2E_PATIENT_EMAIL, password: E2E_PATIENT_PASSWORD });
  if (pe) die(`patient sign-in: ${pe.message}`);
  const { data: cd, error: ce } = await cc.auth.signInWithPassword({ email: E2E_CLINICIAN_EMAIL, password: E2E_CLINICIAN_PASSWORD });
  if (ce) die(`clinician sign-in: ${ce.message}`);
  const patientId = pd.user.id, clinicianId = cd.user.id;

  // Verify the clinician was actually marked verified (the gate for link insert).
  const { data: cp } = await cc.from('clinician_profiles').select('verified').eq('user_id', clinicianId).single();
  if (!cp?.verified) die(`Clinician is not verified yet — run the verify SQL from create mode first.`);

  // Clinician creates the link (status defaults 'pending', initiated_by 'clinician').
  let linkId;
  const { data: ins, error: insErr } = await cc
    .from('clinician_patient_links')
    .insert({ clinician_id: clinicianId, patient_user_id: patientId })
    .select('id').single();
  if (insErr) {
    // Maybe it already exists from a prior run — look it up.
    const { data: existing } = await cc.from('clinician_patient_links').select('id')
      .eq('clinician_id', clinicianId).eq('patient_user_id', patientId).single();
    if (!existing) die(`link insert failed: ${insErr.message}`);
    linkId = existing.id;
    console.log(`  link already existed (${linkId.slice(0, 8)}…)`);
  } else {
    linkId = ins.id;
    console.log(`  ✓ clinician created pending link (${linkId.slice(0, 8)}…)`);
  }

  // Patient approves → active.
  const { error: upErr } = await pc.from('clinician_patient_links')
    .update({ status: 'active', consented_at: new Date().toISOString() })
    .eq('id', linkId);
  if (upErr) die(`patient approve failed: ${upErr.message}`);
  console.log(`  ✓ patient approved → active\n`);
  console.log(`Link ready. Now run:\n  node --env-file=.env --env-file=.env.e2e scripts/chat-realtime-e2e.mjs\n`);
}

(mode === 'link' ? link() : create()).catch((e) => die(`Unhandled: ${e?.message ?? e}`));
